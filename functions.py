# in python there are multiple types of functions

# user defined functions, syntax below

# def function_name (parameters):
#             do something

def add_numbers(first, second):
    print(first + second)


add_numbers(1, 2)  # calling the function


# sending input values directly

def subtract_values(first=33, second=28):
    print(first - second)


subtract_values()  # function call
